// Testing User Interface
static void show_usage(void){
    bd_addr_t iut_address;
    gap_local_bd_addr(iut_address);

    printf("\n--- Bluetooth OPP Client Test Console %s ---\n", bd_addr_to_str(iut_address));
    printf("\n");
    printf("a - establish OPP connection to %s\n", bd_addr_to_str(remote_addr));
    printf("d - pull default object (owner vcard)\n");
    printf("p - push text/plain object\n");
    printf("i - push image/jpeg object\n");
    printf("v - push text/x-vcard object\n");
    printf("t - disconnect\n");
    printf("x - abort operation\n");
    printf("\n");
}

static void stdin_process(char c){
    uint8_t ret;
    switch (c){
        case '\n':
        case '\r':
            break;
        case 'a':
            printf("[+] Connecting to %s...\n", bd_addr_to_str(remote_addr));
            opp_connect(&packet_handler, remote_addr, &opp_cid);
            break;
        case 'd':
            printf("[+] Pulling default Object");
            ret = opp_pull_default_object(opp_cid);
            printf(" (%02x)\n", ret);
            break;
        case 'p':
            printf("[+] Pushing text/plain Object");
            ret = opp_push_object(opp_cid, "hello.txt", "text/plain", (uint8_t *) "huhu!\n", 6);
            printf(" (%02x)\n", ret);
            break;
        case 'i':
            printf("[+] Pushing image/jpeg Object");
            ret = opp_push_object(opp_cid, "git-pull.jpg", "image/jpeg", test_jpg_image, sizeof (test_jpg_image));
            printf(" (%02x)\n", ret);
            break;
        case 'v':
            printf("[+] Pushing text/x-vcard Object");
            ret = opp_push_object(opp_cid, "contact.vcf", "text/x-vcard", test_vcard, sizeof (test_vcard));
            printf(" (%02x)\n", ret);
            break;
        case 'x':
            printf("[+] Abort\n");
            opp_abort(opp_cid);
            break;
        case 't':
            opp_disconnect(opp_cid);
            break;
        default:
            show_usage();
            break;
    }
}

// packet handler for interactive console
static void packet_handler(uint8_t packet_type, uint16_t channel, uint8_t *packet, uint16_t size){
    UNUSED(channel);
    UNUSED(size);
    int i;
    uint8_t status;
    switch (packet_type){
        case HCI_EVENT_PACKET:
            switch (hci_event_packet_get_type(packet)) {
                case BTSTACK_EVENT_STATE:
                    // BTstack activated, get started 
                    if (btstack_event_state_get_state(packet) == HCI_STATE_WORKING){
                        show_usage();
                    }
                    break;
                case HCI_EVENT_OPP_META:
                    switch (hci_event_opp_meta_get_subevent_code(packet)){
                        case OPP_SUBEVENT_CONNECTION_OPENED:
                            status = opp_subevent_connection_opened_get_status(packet);
                            if (status){
                                printf("[!] Connection failed, status 0x%02x\n", status);
                            } else {
                                printf("[+] Connected\n");
                            }
                            break;
                        case OPP_SUBEVENT_CONNECTION_CLOSED:
                            printf("[+] Connection closed\n");
                            break;
                        case OPP_SUBEVENT_OPERATION_COMPLETED:
                            printf("[+] Operation complete, status 0x%02x\n",
                                   opp_subevent_operation_completed_get_status(packet));
                            break;
                        default:
                            break;
                    }
                    break;
                default:
                    break;
            }
            break;
        case OPP_DATA_PACKET:
            for (i=0;i<size;i++){
                printf("%c", packet[i]);
            }
            break;
        default:
            break;
    }
}
